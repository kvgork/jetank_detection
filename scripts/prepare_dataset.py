#!/usr/bin/env python3
"""Build a YOLO train/val dataset from web-labeller captures.

The web control labeller (``jetank_web_control``) saves a flat directory of
``<name>.jpg`` images with YOLO ``<name>.txt`` sidecars plus a ``classes.txt``.
Ultralytics ``yolo train`` instead wants an ``images/{train,val}`` +
``labels/{train,val}`` layout described by a ``data.yaml``. This script bridges
the two — it is step **S2** of ``sock-sim-autotrain-plan.md``.

Rules:
  * Only images that have a matching ``.txt`` sidecar are used (an image with no
    sidecar is *not labelled yet*, NOT a negative — it is skipped). An **empty**
    sidecar IS a negative (background, no object) and is kept.
  * Split is deterministic (seeded) so re-runs are reproducible.
  * Images/labels are **symlinked** (not copied) to save disk; pass --copy to
    copy instead (e.g. when shipping the dataset to Colab).

Usage::

    python prepare_dataset.py \
        --src ~/datasets/detection \
        --out ~/datasets/detection/yolo \
        --val-frac 0.2 --names sock

Then::

    yolo train model=yolo11n.pt data=~/datasets/detection/yolo/data.yaml \
        epochs=100 imgsz=640 batch=16
"""
import argparse
import os
import random
import shutil
from pathlib import Path

IMG_EXTS = ('.jpg', '.jpeg', '.png')


def collect_pairs(src: Path):
    """Return (image, label) Paths for every image that has a sidecar."""
    pairs, unlabelled = [], 0
    for img in sorted(src.iterdir()):
        if img.suffix.lower() not in IMG_EXTS:
            continue
        label = img.with_suffix('.txt')
        if label.name == 'classes.txt':
            continue
        if label.exists():
            pairs.append((img, label))
        else:
            unlabelled += 1
    return pairs, unlabelled


def link_or_copy(src: Path, dst: Path, copy: bool):
    dst.parent.mkdir(parents=True, exist_ok=True)
    if dst.exists() or dst.is_symlink():
        dst.unlink()
    if copy:
        shutil.copy2(src, dst)
    else:
        os.symlink(src.resolve(), dst)


def main():
    ap = argparse.ArgumentParser(description=__doc__,
                                 formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument('--src', default='~/datasets/detection',
                    help='Flat capture dir with <name>.jpg + <name>.txt sidecars')
    ap.add_argument('--out', default='~/datasets/detection/yolo',
                    help='Output dataset root (images/ + labels/ + data.yaml)')
    ap.add_argument('--val-frac', type=float, default=0.2,
                    help='Fraction of labelled images held out for validation')
    ap.add_argument('--names', nargs='+', default=['sock'],
                    help='Class names in class-index order (index 0 first)')
    ap.add_argument('--seed', type=int, default=42, help='Split RNG seed')
    ap.add_argument('--copy', action='store_true',
                    help='Copy files instead of symlinking (for Colab/transfer)')
    args = ap.parse_args()

    src = Path(os.path.expanduser(args.src))
    out = Path(os.path.expanduser(args.out))
    if not src.is_dir():
        raise SystemExit(f'src not found: {src}')

    pairs, unlabelled = collect_pairs(src)
    if not pairs:
        raise SystemExit(f'no labelled images in {src} (need .jpg + matching .txt)')

    negatives = sum(1 for _, lbl in pairs if lbl.stat().st_size == 0)
    rng = random.Random(args.seed)
    rng.shuffle(pairs)
    n_val = max(1, round(len(pairs) * args.val_frac))
    val, train = pairs[:n_val], pairs[n_val:]

    # Clean previous split dirs so stale links don't linger.
    for sub in ('images/train', 'images/val', 'labels/train', 'labels/val'):
        d = out / sub
        if d.exists():
            shutil.rmtree(d)

    for split, items in (('train', train), ('val', val)):
        for img, lbl in items:
            link_or_copy(img, out / 'images' / split / img.name, args.copy)
            link_or_copy(lbl, out / 'labels' / split / lbl.name, args.copy)

    names_block = '\n'.join(f'  {i}: {n}' for i, n in enumerate(args.names))
    data_yaml = out / 'data.yaml'
    data_yaml.write_text(
        f'# Generated by prepare_dataset.py (S2). Do not hand-edit paths.\n'
        f'path: {out}\n'
        f'train: images/train\n'
        f'val: images/val\n'
        f'names:\n{names_block}\n'
    )

    print(f'src           : {src}')
    print(f'labelled      : {len(pairs)} (negatives/empty: {negatives})')
    print(f'unlabelled    : {unlabelled} (skipped — not yet annotated)')
    print(f'train / val   : {len(train)} / {len(val)}  (val-frac {args.val_frac}, seed {args.seed})')
    print(f'classes       : {args.names}')
    print(f'data.yaml     : {data_yaml}')
    print(f'\nlink mode     : {"copy" if args.copy else "symlink"}')
    print(f'\nNext:\n  yolo train model=yolo11n.pt data={data_yaml} epochs=100 imgsz=640 batch=16')


if __name__ == '__main__':
    main()
